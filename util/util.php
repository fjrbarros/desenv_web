<?php
function getHead()
{ 
    $html = '<meta charset="UTF-8">';
    $html .= '<meta name="viewport" content="width=device-width, initial-scale=1.0">';
    $html .= '<link rel="preconnect" href="https://fonts.gstatic.com">';
    $html .= '<link href="https://fonts.googleapis.com/css2?family=Volkhov&display=swap" rel="stylesheet">';
    $html .= '<link rel="stylesheet" href="/desenv_web/styles/reset.css">';

    return $html;
}
